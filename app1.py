def IsUpper(char):
    # Проверяем, что символ находится в диапазоне от 'A' до 'Z'
    return len(char) == 1 and 'A' <= char <= 'Z'

# Ввод данных от пользователя
input_char = input("Введите символ: ")

# Проверка и вывод результата
if IsUpper(input_char):
    print("Символ является заглавной буквой латинского алфавита.")
else:
    print("Символ НЕ является заглавной буквой латинского алфавита.")